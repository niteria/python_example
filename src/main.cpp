namespace py = pybind11;

auto update_name(char* name, int idx) -> void {
  auto idx_start = strlen("VALUE");
  name[idx_start] = (idx / 100) % 10 + '0';
  name[idx_start + 1] = (idx / 10) % 10 + '0';
  name[idx_start + 2] = idx % 10 + '0';

}

template <typename EnumT>
auto mk_enum(py::module m, EnumT start, EnumT end, char* enum_name) -> void {
  auto small = py::enum_<EnumT>(m, enum_name);
  char name[] = "VALUE000";
  for (auto val = static_cast<int>(start);
       val <= static_cast<int>(end);
       ++val) {
    update_name(name, val);
    small.value(name, static_cast<EnumT>(val));
  }
}

